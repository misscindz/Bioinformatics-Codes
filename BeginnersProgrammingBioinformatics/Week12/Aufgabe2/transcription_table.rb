  def TranscriptionTable.translate(orf)
    if orf.length.modulo(3) != 0
      raise "orf should have a length that is a multiple of 3!"
    end
    aas = ""
    found_stop = false
    orf.scan(/.{3}/) do |triplet|
      if found_stop
        raise "found stop within orf, should only apear at end!"
      end
      aas += @@transcription_table[triplet.upcase.tr('U', 'T')]
      found_stop = aas[-1] == '_'
    end
    return aas
  end
end

